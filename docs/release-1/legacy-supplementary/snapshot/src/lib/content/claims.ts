function makeSupplementalClaim(
  contentId: string,
  sourceUrl: string | undefined,
  title: string,
  explanation: string,
  topicFamily: string,
): ContentClaim {
  const safeId = contentId.replace(/[^a-z0-9:]+/gi, '-').toLowerCase();
  const source = sourceUrl ?? 'https://react.dev/learn';
  return {
    claimId: `claim-${safeId.replace(':', '-')}`,
    contentId,
    claim: title,
    sourceUrl: source,
    sourceType: source.includes('w3.org') ? 'standard' : 'official',
    frameworkVersion: source.includes('nextjs.org') ? 'Next.js 15.5.20' : source.includes('react.dev') ? 'React 19.2.7' : `Official ${topicFamily} documentation`,
    lastVerifiedAt: claimReviewDate,
    supports: explanation,
    conflicts: 'This study aid is scoped to the stated topic and does not replace the linked technical documentation.',
    confidence: 'high',
  };
}

export function validateVolatileClaims(claims: readonly ContentClaim[] = volatileClaims): readonly string[] {
  const catalog = buildContentCatalog();
  const records = new Map(catalog.map((record) => [record.id, record]));
  const seen = new Set<string>();
  const issues: string[] = [];

  for (const claim of claims) {
    if (seen.has(claim.claimId)) issues.push(`${claim.claimId}: duplicate claim ID`);
    seen.add(claim.claimId);
    const record = records.get(claim.contentId);
    if (!record) issues.push(`${claim.claimId}: unknown content ID ${claim.contentId}`);
    const source = record?.sourceMetadata.find((item) => item.sourceUrl === claim.sourceUrl);
    if (!source) {
      issues.push(`${claim.claimId}: source is not attached to ${claim.contentId}`);
    }
    if (source && source.sourceType !== claim.sourceType) {
      issues.push(`${claim.claimId}: source type does not match catalog metadata`);
    }
    if (claim.sourceType === 'community') issues.push(`${claim.claimId}: community source is not allowed`);
    if (!claim.frameworkVersion.trim()) issues.push(`${claim.claimId}: framework version is empty`);
    if (!claim.lastVerifiedAt || Number.isNaN(Date.parse(claim.lastVerifiedAt))) issues.push(`${claim.claimId}: invalid review date`);
    if (!claim.supports.trim()) issues.push(`${claim.claimId}: support note is empty`);
    if (!claim.conflicts.trim()) issues.push(`${claim.claimId}: conflict note is empty`);
  }

  return issues;
}

export interface ClaimCoverageReport {
  readonly claimCount: number;
  readonly coveredContentIds: readonly string[];
  readonly uncoveredContentIds: readonly string[];
  readonly coverageRatio: number;
  readonly issues: readonly string[];
  readonly ok: boolean;
}

export function validateClaimCoverage(claims: readonly ContentClaim[] = volatileClaims): ClaimCoverageReport {
  const catalog = buildContentCatalog();
  const contentIds = new Set(catalog.map((record) => record.id));
  const covered = new Set<string>();
  const seenClaimIds = new Set<string>();
  const issues: string[] = [];

  for (const claim of claims) {
    const normalizedClaimId = claim.claimId.trim().toLowerCase();
    if (claim.claimId !== normalizedClaimId || /\s/.test(claim.claimId)) {
      issues.push(`${claim.claimId}: claim ID is not normalized`);
    }
    if (seenClaimIds.has(normalizedClaimId)) issues.push(`${normalizedClaimId}: duplicate claim ID`);
    seenClaimIds.add(normalizedClaimId);

    const normalizedContentId = claim.contentId.trim().toLowerCase();
    if (claim.contentId !== normalizedContentId || /\s/.test(claim.contentId)) {
      issues.push(`${claim.contentId}: content ID is not normalized`);
    }
    if (!contentIds.has(claim.contentId)) {
      issues.push(`${claim.claimId}: unknown content ID ${claim.contentId}`);
    } else {
      covered.add(claim.contentId);
    }
  }

  const coveredContentIds = catalog.filter((record) => covered.has(record.id)).map((record) => record.id);
  const uncoveredContentIds = catalog.filter((record) => !covered.has(record.id)).map((record) => record.id);
  const coverageRatio = catalog.length === 0 ? 0 : coveredContentIds.length / catalog.length;
  return {
    claimCount: claims.length,
    coveredContentIds,
    uncoveredContentIds,
    coverageRatio,
    issues,
    ok: issues.length === 0,
  };
}
